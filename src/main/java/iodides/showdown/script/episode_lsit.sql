CREATE TABLE EPISODE_LIST(
	SID 		VARCHAR(255)	NOT NULL,
	TITLE		VARCHAR(255)	NOT NULL DEFAULT '',
	EPINUM		INT(11)			NOT NULL DEFAULT '0',
	AIR			VARCHAR(255)	NOT NULL DEFAULT '',
    ATIME       TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UTIME       TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    HD_FIND     TINYINT(3)      NOT NULL DEFAULT '0',
	HD_DOWN		TINYINT(3)      NOT NULL DEFAULT '0',
	HD_COMP		TINYINT(3)      NOT NULL DEFAULT '0',
	HD_DEL		TINYINT(3)      NOT NULL DEFAULT '0',
	HD_MAGNET	VARCHAR(255)	NOT NULL DEFAULT '',
    HD_HASH     VARCHAR(255)	NOT NULL DEFAULT '',
    FHD_FIND    TINYINT(3)      NOT NULL DEFAULT '0',
	FHD_DOWN	TINYINT(3)      NOT NULL DEFAULT '0',
	FHD_COMP	TINYINT(3)      NOT NULL DEFAULT '0',
	FHD_DEL		TINYINT(3)      NOT NULL DEFAULT '0',
	FHD_MAGNET	VARCHAR(255)	NOT NULL DEFAULT '',
    FHD_HASH    VARCHAR(255)	NOT NULL DEFAULT '',
	PRIMARY KEY(SID, EPINUM)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;