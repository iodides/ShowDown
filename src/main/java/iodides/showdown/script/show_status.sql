CREATE TABLE SHOW_STATUS(
	SID   		VARCHAR(255)	NOT NULL,
	QUALITY		VARCHAR(255)	NOT NULL DEFAULT '',
	MONITOR     TINYINT(3)      NOT NULL DEFAULT '0',
	CEPI		INT(11)			NOT NULL DEFAULT '0',	-- 현재까지 완료된 에피소드 개수
	TEPI		INT(11)			NOT NULL DEFAULT '0',	-- 전체 에피소드 개수
	RELGROUP	VARCHAR(255)	NOT NULL DEFAULT '',
	PRIMARY KEY(SID, QUALITY)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;