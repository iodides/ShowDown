CREATE TABLE SHOW_LIST(
	SID   		VARCHAR(255)	NOT NULL,
	STYPE		VARCHAR(255)	NOT NULL DEFAULT '',
	TITLE		VARCHAR(255)	NOT NULL DEFAULT '',
	KWORD		VARCHAR(255)	NOT NULL DEFAULT '',
	SEASON		INT(11)			NOT NULL DEFAULT '1',
	AIRDATE		VARCHAR(255)	NOT NULL DEFAULT '',
	EPICOUNT	INT(11)			NOT NULL DEFAULT '0',
    ATIME       TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UTIME       TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
	COMPANY		VARCHAR(255)	NOT NULL DEFAULT '',
	SCHEDULE	VARCHAR(255)	NOT NULL DEFAULT '',
	STATUS		VARCHAR(255)	NOT NULL DEFAULT '',
	GENRE		VARCHAR(255)	NOT NULL DEFAULT '',
	COMMENT		TEXT(10000)		DEFAULT NULL,
	URL			TEXT(10000)		DEFAULT NULL,
	THUMB		TEXT(10000)		DEFAULT NULL,
	RELGROUP	VARCHAR(255)	NOT NULL DEFAULT '',
	HD  		VARCHAR(255)	NOT NULL DEFAULT 'F',
	FHD			VARCHAR(255)	NOT NULL DEFAULT 'F',
	COMPLETE	VARCHAR(255)	NOT NULL DEFAULT 'F',
	PRIMARY KEY(SID),
	INDEX IDX_STYPE(STYPE),
	INDEX IDX_TITLE(TITLE)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;