CREATE TABLE `TORRENT_LIST` (
  `SITENAME` varchar(255) NOT NULL,
  `ID` varchar(255) NOT NULL,
  `TITLE` varchar(255) NOT NULL DEFAULT '',
  `FLAG` varchar(255) NOT NULL DEFAULT 'F',
  `NAME` varchar(255) NOT NULL DEFAULT '',
  `EPI1` int(11) NOT NULL DEFAULT '0',
  `EPI2` int(11) NOT NULL DEFAULT '0',
  `AIR` varchar(255) NOT NULL DEFAULT '',
  `QUALITY` varchar(255) DEFAULT '',
  `RELGROUP` varchar(255) NOT NULL DEFAULT '',
  `URL` varchar(255) NOT NULL DEFAULT '',
  `MAGNET` varchar(255) NOT NULL DEFAULT '',
  `ADDTIME` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`SITE`,`ID`),
  KEY `IDX_NAME` (`NAME`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;
